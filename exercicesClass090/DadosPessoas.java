package exercicesClass090;

public class DadosPessoas {
    private double height;
    private char gender;

    public DadosPessoas (double height, char gender) {
        this.height = height;
        this.gender = gender;
    }

    public double getHeight() {
        return height;
    }

    public char getGender() {
        return gender;
    }

}
